create table fraud_detection_results (
    id bigint generated by default as identity primary key,
    event_id varchar(100) not null,
    trace_id varchar(150) not null,
    user_id varchar(100) not null,
    account_id varchar(100),
    risk_score integer not null,
    risk_level varchar(30) not null,
    decision varchar(30) not null,
    matched_rules text,
    reason text,
    detected_at timestamp with time zone not null,
    created_at timestamp with time zone not null,
    updated_at timestamp with time zone not null,
    constraint uk_fraud_detection_results_event_id unique (event_id),
    constraint ck_fraud_detection_results_risk_score_range check (risk_score >= 0 and risk_score <= 100)
);

create index idx_fraud_results_user_id_created_at on fraud_detection_results (user_id, created_at);
create index idx_fraud_results_risk_level_created_at on fraud_detection_results (risk_level, created_at);
create index idx_fraud_results_decision_created_at on fraud_detection_results (decision, created_at);
create index idx_fraud_results_trace_id on fraud_detection_results (trace_id);
